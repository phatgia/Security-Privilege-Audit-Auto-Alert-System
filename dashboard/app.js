// ─── Table Rendering ──────────────────────────────────────
function renderTable() {
    const searchVal = document.getElementById('search-input')?.value.toLowerCase() || '';
    const systemFilter = document.getElementById('system-filter')?.value || 'all';

    let filtered = allUsers.filter(u => {
        const username = (u.username || u.sam_account || '').toLowerCase();
        const matchSearch = !searchVal || username.includes(searchVal);
        const matchRisk = currentFilter === 'all' || u.risk_level === currentFilter;
        const matchSystem = systemFilter === 'all' || u.system === systemFilter;
        return matchSearch && matchRisk && matchSystem;
    });

    // Sort
    filtered.sort((a, b) => {
        let va = a[currentSort.key], vb = b[currentSort.key];
        if (currentSort.key === 'risk_level') {
            va = RISK_ORDER[va] || 0;
            vb = RISK_ORDER[vb] || 0;
        }
        if (typeof va === 'string') va = va.toLowerCase();
        if (typeof vb === 'string') vb = vb.toLowerCase();
        if (va < vb) return currentSort.dir === 'asc' ? -1 : 1;
        if (va > vb) return currentSort.dir === 'asc' ? 1 : -1;
        return 0;
    });

    const tbody = document.getElementById('users-tbody');
    const countEl = document.getElementById('result-count');
    if (countEl) countEl.textContent = `${filtered.length} users`;

    if (filtered.length === 0) {
        tbody.innerHTML = `<tr><td colspan="8" style="text-align:center;padding:40px;color:var(--text-muted)">
            😴 Không tìm thấy kết quả phù hợp
        </td></tr>`;
        return;
    }

    tbody.innerHTML = filtered.map((u, idx) => {
        const username = u.username || u.sam_account || 'Unknown';
        const initials = username.slice(0, 2).toUpperCase();
        const sysInfo = SYSTEM_NAMES[u.system] || { emoji: '🖥️', label: u.system };
        const risk = u.risk_level || 'LOW';
        const days = u.days_inactive;
        const daysClass = days > 180 ? 'critical' : days > 60 ? 'high' : days > 30 ? 'medium' : days < 0 ? 'unknown' : 'low';
        const daysText = days < 0 ? 'Unknown' : `${days}d`;

        const lastLogin = (u.last_login || u.last_logon || '').substring(0, 10) || 'N/A';

        const privileges = [
            ...(u.privilege_source || u.privilege_groups || u.attached_policies || [])
        ].slice(0, 3);
        const privTags = privileges.map(p => `<span class="priv-tag">${p}</span>`).join('') || '—';

        const topAlert = (u.alerts || [])[0] || '—';
        const alertMore = u.alerts?.length > 1 ? ` <span style="color:var(--text-muted)">+${u.alerts.length - 1}</span>` : '';

        const riskEmoji = { CRITICAL: '🔴', HIGH: '🟠', MEDIUM: '🟡', LOW: '🟢' }[risk] || '⚪';

        return `<tr data-idx="${idx}" style="animation: fade-in-row 0.3s ease ${idx * 0.03}s both">
            <td>
                <div class="username-cell">
                    <div class="username-avatar">${initials}</div>
                    <span class="username-text">${username}</span>
                </div>
            </td>
            <td><span class="system-badge">${sysInfo.emoji} ${sysInfo.label}</span></td>
            <td><span class="days-cell ${daysClass}">${daysText}</span></td>
            <td><span class="risk-badge ${risk}">${riskEmoji} ${risk}</span></td>
            <td><span class="last-login-cell">${lastLogin}</span></td>
            <td><div class="privileges-cell">${privTags}</div></td>
            <td><div class="alerts-cell">${topAlert}${alertMore}</div></td>
            <td>
                <button class="btn-detail" onclick="showUserDetail(${JSON.stringify(u).replace(/"/g, '&quot;')})">
                    🔍 Chi tiết
                </button>
            </td>
        </tr>`;
    }).join('');
}

// ─── Filters & Sort ───────────────────────────────────────
function setFilter(filter, el) {
    currentFilter = filter;
    document.querySelectorAll('.chip').forEach(c => c.classList.remove('active'));
    el.classList.add('active');
    renderTable();
}

function filterTable() {
    renderTable();
}

function sortTable(key) {
    if (currentSort.key === key) {
        currentSort.dir = currentSort.dir === 'asc' ? 'desc' : 'asc';
    } else {
        currentSort.key = key;
        currentSort.dir = 'desc';
    }
    renderTable();
}

// ─── Log Section ──────────────────────────────────────────
function updateLogSection(log) {
    if (!log) return;

    const sudoEl  = document.getElementById('log-sudo');
    const sshEl   = document.getElementById('log-ssh');
    const bruteEl = document.getElementById('log-brute');

    if (sudoEl) sudoEl.textContent = log.total_sudo_events || 0;
    if (sshEl)  sshEl.textContent  = log.total_ssh_logins  || 0;

    const suspicious = log.suspicious_accounts || [];
    if (bruteEl) bruteEl.textContent = suspicious.length;

    const bruteForcedContainer = document.getElementById('brute-force-list');
    if (bruteForcedContainer && suspicious.length > 0) {
        const failed = log.failed_login_accounts || {};
        bruteForcedContainer.innerHTML = `
            <div class="brute-force-section">
                <h3>🚨 Tài Khoản Bị Brute Force Tấn Công</h3>
                ${suspicious.map(acc =>
                    `<div class="brute-item">
                        <span>⚠️</span>
                        <span class="brute-username">${acc}</span>
                        <span class="brute-count">${failed[acc] || 0} lần thất bại</span>
                    </div>`
                ).join('')}
            </div>`;
    }
}

// ─── User Detail Modal ────────────────────────────────────
function showUserDetail(user) {
    const modal = document.getElementById('modal-overlay');
    const title = document.getElementById('modal-title');
    const body  = document.getElementById('modal-body');

    const username = user.username || user.sam_account || 'Unknown';
    const risk = user.risk_level || 'LOW';
    const riskEmoji = { CRITICAL: '🔴', HIGH: '🟠', MEDIUM: '🟡', LOW: '🟢' }[risk] || '⚪';
    const sysInfo = SYSTEM_NAMES[user.system] || { emoji: '🖥️', label: user.system };

    title.innerHTML = `${riskEmoji} ${username} <span style="font-size:0.75rem;color:var(--text-secondary);font-weight:400">${sysInfo.emoji} ${sysInfo.label}</span>`;

    const fields = [
        ['Username', `<code style="font-family:monospace;color:var(--accent-light)">${username}</code>`],
        ['Hệ thống', `${sysInfo.emoji} ${sysInfo.label}`],
        ['Risk Level', `<span class="risk-badge ${risk}">${riskEmoji} ${risk}</span>`],
        ['Days Inactive', `${user.days_inactive >= 0 ? user.days_inactive + ' ngày' : 'Unknown'}`],
        ['Last Login', user.last_login || user.last_logon || 'N/A'],
        ['Email', user.email || 'N/A'],
        ['Account Enabled', user.account_enabled !== false ? '✅ Enabled' : '❌ Disabled'],
        ['MFA Active', user.mfa_active !== undefined ? (user.mfa_active ? '✅ Yes' : '❌ No') : 'N/A'],
        ['Full Admin Access', user.full_access || user.has_admin_access ? '⚠️ Yes' : 'No'],
        ['NOPASSWD Sudo', user.nopasswd ? '⚠️ Yes' : 'No'],
        ['Key Age', user.key_age_days > 0 ? `${user.key_age_days} ngày` : 'N/A'],
    ].filter(([_, v]) => v !== 'N/A' && v !== 'No' && v !== undefined);

    const alerts = user.alerts || [];

    body.innerHTML = `
        <div style="margin-bottom:8px">
            ${fields.map(([label, val]) =>
                `<div class="modal-field">
                    <span class="modal-field-label">${label}</span>
                    <span class="modal-field-value">${val}</span>
                </div>`
            ).join('')}
        </div>
        ${alerts.length > 0 ? `
        <div class="modal-alerts">
            <h3>📋 Risk Alerts</h3>
            ${alerts.map(a => `<div class="alert-item">${a}</div>`).join('')}
        </div>` : ''}
        ${user.notes ? `
        <div style="margin-top:16px;padding:12px;background:var(--bg-input);border-radius:8px;font-size:0.85rem;color:var(--text-secondary)">
            📝 ${user.notes}
        </div>` : ''}
    `;

    modal.classList.add('open');
}

function closeModal() {
    document.getElementById('modal-overlay')?.classList.remove('open');
}

document.addEventListener('keydown', e => {
    if (e.key === 'Escape') closeModal();
});

// ─── Export ───────────────────────────────────────────────
function exportJSON() {
    if (!currentData) return;
    const blob = new Blob([JSON.stringify(currentData, null, 2)], { type: 'application/json' });
    const url = URL.createObjectURL(blob);
    const a = document.createElement('a');
    a.href = url;
    a.download = `privilege_audit_${new Date().toISOString().slice(0,10)}.json`;
    a.click();
    URL.revokeObjectURL(url);
    showToast('📥 Đang tải xuống file JSON...');
}

function refreshData() {
    showToast('🔄 Đang tải lại dữ liệu...');
    loadData();
}

// ─── Toast ────────────────────────────────────────────────
function showToast(message, duration = 3000) {
    const container = document.getElementById('toast-container');
    const toast = document.createElement('div');
    toast.className = 'toast';
    toast.textContent = message;
    container.appendChild(toast);
    setTimeout(() => {
        toast.style.opacity = '0';
        toast.style.transform = 'translateX(100%)';
        toast.style.transition = 'all 0.3s ease';
        setTimeout(() => toast.remove(), 300);
    }, duration);
}

