const RISK_ORDER = { CRITICAL: 4, HIGH: 3, MEDIUM: 2, LOW: 1 };

// ─── Data Loading ─────────────────────────────────────────
async function loadData() {
    // Try to load from reports/dashboard_data.json (generated by Python)
    try {
        const response = await fetch('../reports/dashboard_data.json');
        if (response.ok) {
            const data = await response.json();
            processData(data);
            return;
        }
    } catch (e) {}

    // Fallback: load embedded demo data
    console.log('Loading demo data...');
    processData(getDemoData());
}

function processData(data) {
    currentData = data;
    allUsers = [];

    const systems = data.systems || [];

    // Flatten all users from all systems
    for (const report of systems) {
        const system = report.system;
        const users = report.users || [];
        for (const user of users) {
            allUsers.push({ ...user, system });
        }
    }

    // Update stats
    updateStats(systems);
    renderCharts(systems);
    renderTable();
    updateLogSection(data.log_analysis);
    updateScanTime(data.generated_at);
}

function updateStats(systems) {
    let totalPrivileged = 0, totalInactive = 0, totalCritical = 0;

    for (const r of systems) {
        const s = r.summary || {};
        totalPrivileged += s.total_privileged_users || 0;
        totalInactive   += (s.inactive_users || s.inactive_privileged_users || 0);
        totalCritical   += s.critical_users || 0;
    }

    animateNumber('num-critical',   totalCritical);
    animateNumber('num-inactive',   totalInactive);
    animateNumber('num-privileged', totalPrivileged);
    animateNumber('num-systems',    systems.length);
}

function animateNumber(id, target) {
    const el = document.getElementById(id);
    if (!el) return;
    const start = parseInt(el.textContent) || 0;
    const duration = 600;
    const startTime = performance.now();

    function update(now) {
        const elapsed = now - startTime;
        const progress = Math.min(elapsed / duration, 1);
        const eased = 1 - Math.pow(1 - progress, 3); // ease-out cubic
        el.textContent = Math.round(start + (target - start) * eased);
        if (progress < 1) requestAnimationFrame(update);
    }
    requestAnimationFrame(update);
}

function updateScanTime(ts) {
    const el = document.getElementById('scan-time-text');
    if (!el || !ts) return;
    try {
        const d = new Date(ts);
        el.textContent = d.toLocaleString('vi-VN');
    } catch (e) {
        el.textContent = ts;
    }
}

